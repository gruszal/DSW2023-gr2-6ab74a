# lista może mieć elementy różnych typów
imiona_i_inne = ['Artur', 'Barbara', 23, True, 3.6]
print(imiona_i_inne)

# lista może nie mieć żadnego elementu
pusta_lista = []
print(pusta_lista)

# elementy mogą się powtarzać
imie = ['Artur', 'Artur', 'Artur']
print(imie)

imie2 = ['Basia'] * 5
print(imie2)

imiona_suma = ['Artur', 'Basia'] + ['Czesław', 'Damian']
print(imiona_suma)
