prawda = True
falsz = False

print(prawda and prawda)  # spójnik logiczny 'i' zwraca True tylko, gdy obie wartości są prawdziwe
print(prawda and falsz)
print(falsz and falsz)

print()

print(prawda or falsz)  # spójnik logiczny 'lub' zwraca True, gdy chociaż jedna wartość jest prawdziwa
print(prawda or falsz)
print(falsz or falsz)